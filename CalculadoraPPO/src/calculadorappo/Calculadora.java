        public static boolean hpunto(String cad)
            {
                boolean res;
                res=false;
                //recorro todo pra encontrar un punto
                    for(int i=0; i < cad.length(); i++ ){
                        if (cad.substring(i, i+1).equals(".")){
                         res=true;
                         break;
                        }
                    }
                    return res;
            }
    
    
    
    
    
    
